package edu.gzhu.fuckyk.pojo;

public class Admintor {

	private int admintor_id;
	private String name;
	private String password;

	public int getAdmintor_id() {
		return admintor_id;
	}

	public void setAdmintor_id(int admintor_id) {
		this.admintor_id = admintor_id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
